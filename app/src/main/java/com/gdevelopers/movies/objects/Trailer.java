package com.gdevelopers.movies.objects;

public class Trailer {
    private String name;
    private String source;

    public Trailer() {
    }

    public String getName() {
        return name;
    }


    public void setName(String name) {
        this.name = name;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

}
